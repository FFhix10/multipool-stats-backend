package strat.mining.multipool.stats.client.util;

public class Pair<M, N> {

	private M left;
	private N right;

	public Pair(M left, N right) {
		this.left = left;
		this.right = right;
	}

	public M getLeft() {
		return left;
	}

	public void setLeft(M left) {
		this.left = left;
	}

	public N getRight() {
		return right;
	}

	public void setRight(N right) {
		this.right = right;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((left == null) ? 0 : left.hashCode());
		result = prime * result + ((right == null) ? 0 : right.hashCode());
		return result;
	}

	@SuppressWarnings("rawtypes")
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Pair other = (Pair) obj;
		if (left == null) {
			if (other.left != null)
				return false;
		} else if (!left.equals(other.left))
			return false;
		if (right == null) {
			if (other.right != null)
				return false;
		} else if (!right.equals(other.right))
			return false;
		return true;
	}

}
