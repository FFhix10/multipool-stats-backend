package strat.mining.multipool.stats.persistence.model.donation;

import java.util.Collection;
import java.util.Date;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.Indexed;
import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "Address")
public class Address {

	@Id
	private Integer id;

	@Indexed
	private String address;

	@Indexed
	private Date lastUpdated;

	private Collection<String> transactionIds;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Collection<String> getTransactionIds() {
		return transactionIds;
	}

	public void setTransactionIds(Collection<String> transactionIds) {
		this.transactionIds = transactionIds;
	}

	public Date getLastUpdated() {
		return lastUpdated;
	}

	public void setLastUpdated(Date lastUpdated) {
		this.lastUpdated = lastUpdated;
	}

}
